function sum(a,b){
    return a+b;
}

function subtracao (a,b){
    return a-b;
}

function multiplicacao (a,b){
    return a*b;
}

function divisão (a,b){
    return a/b;
}

function resto (a,b){
    return a%b;
}

var a = 3;
var b = 2;

console.log(sum(a,b));
console.log(subtracao(a,b));
console.log(multiplicacao(a,b));
console.log(divisão(a,b));
console.log(resto(a,b));